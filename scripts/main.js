window.onload = function()
{
    var canvas = document.getElementById("paper");
    var ctx = canvas.getContext("2d");

    var X = canvas.width;
    var Y = canvas.height;

    var draw = new Draw(ctx, X, Y);

    var PI = Math.PI;
    var TWO_PI = 2 * Math.PI;

    var FPS = 20;

    var run = function()
    {
        if (getIsStarted())
        {
            draw.clearRect(0, 0, X, Y);
            for (var i=1; i<teapotPoints.length; i++) {
                var col = "blue";
                draw.drawCircle(teapotPoints[i-1].x, teapotPoints[i-1].y, 1, col);
                draw.drawLine(teapotPoints[i], teapotPoints[i-1], "black");
                //rotatePointX(teapotPoints[i], toRad(1));
                //rotatePointY(teapotPoints[i], -PI/64);
                //rotatePointZ(shape[i], PI/32);
            }
        }
    }

    // create points
    teapotPoints = [];
    for (var i=0; i<teapot.length; i++)
    {
        var ptData = teapot[i];
        var pt = new Point(ptData[0], ptData[1], ptData[2]);
        teapotPoints.push(pt);
    }

    for (var i=0; i<teapotPoints.length; i++)
    {
        // center obj to origin
        teapotPoints[i].x *= 100;
        teapotPoints[i].y *= 100;
        teapotPoints[i].z *= 100;
        rotatePointX(teapotPoints[i], toRad(-90));
        rotatePointY(teapotPoints[i], toRad(180));
        // 'Magic' rotation numbers, 45deg and 35.26438968deg
        applyOrthoPoint(teapotPoints[i]);
    }

    run()
}
